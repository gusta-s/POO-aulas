public class variavel2 {
    public static void main(String[] args) {
        
        // TIPOS DE VARIAVEIS  
        int valor = 100000; // variavel padrao do tipo inteira (32 bits)
        byte valor2 = 100; // variavel do tipo inteira para valores bem pequenos (8 bits)
        short valor3 = 10000; // variavel do tipo inteira para valores pequenos (16 bits)
        long valor4 = 100000000000L; // variavel do tipo inteira para valores grandes (64 bits)

        float valor5 = 10.5f; // variavel do tipo real (32 bits)
        double valor6 = 10.5555555555d; // variavel do tipo real com mais casas decimais (64 bits)

        boolean valor7 = true; // variavel do tipo logica, aceitando valores verdadeiros ou falsos

        char letra = 'r'; // armazena uma unica letra
    }   
}
