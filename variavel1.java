public class variavel1{
    public static void main(String[] args){
        
        // DECLARACAO DE VARIAVEIS

        /* Para declarar uma variavel escreve-se o tipo da variavel
         * e depois o seu nome, com um ";" ao fim
        */

        int variavel; // uma variavel com nome simples, por convencao, se inicia coma letra minuscula
        
        int outraVariavel; /* uma variavel com nome composto, tem o primeiro nome iniciado 
        com letra minuscula e o segundo, com letra maiuscula */
        
        variavel = 50; // variavel recebe valor 
        outraVariavel = 200; // variavel recebe valor

        System.out.println(variavel);
        System.out.println(outraVariavel);
         
    }
}